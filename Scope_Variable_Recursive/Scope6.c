#include <stdio.h>
void func() {
    static int a = 1; // 정적 지역변수 a = 1 (최초 1회만 초기화, 값 유지)
    int b = 1;        // 일반 지역변수 b = 1 (호출될 때마다 매번 초기화)
    a++;              // a에 1을 더함 (값 유지됨)
    b++;              // b에 1을 더함 (다음 호출 시 다시 1로 초기화)
    printf("%d %d\n", a, b); // a, b 출력
}

int main() {
    func(); // [1] a = 1+1 = 2, b = 1+1 = 2 → 출력: 2 2
    func(); // [2] a = 2+1 = 3, b = 1+1 = 2 → 출력: 3 2
    func(); // [3] a = 3+1 = 4, b = 1+1 = 2 → 출력: 4 2
    return 0;
}

// 실행 결과:
// 2 2
// 3 2
// 4 2