#include <stdio.h>
int f(int n) {
    if (n <= 1) return 1;         // 기저조건: n이 0 또는 1이면 1 반환
    return f(n-1) + f(n-2) * 2;  // f(n-1) + f(n-2)*2 반환
}

int main(void) {
    printf("%d\n", f(4));         // f(4) 호출 → 결과: 11
    return 0;
}

// ── 재귀 트리 ─────────────────────────────────
//
//              f(4)
//             /    \(×2)
//           f(3)   f(2)
//          /  \(×2)
//        f(2) f(1)
//       /  \(×2)
//     f(1) f(0)
//
// (×2) 표시된 가지 = f(n-2) * 2 로 사용됨

// ── 기저조건 ──────────────────────────────────
// f(0) = 1
// f(1) = 1

// ── 복귀하며 계산 ─────────────────────────────
// f(2) = f(1) + f(0)*2 = 1 + 1*2 =  3
// f(3) = f(2) + f(1)*2 = 3 + 1*2 =  5
// f(4) = f(3) + f(2)*2 = 5 + 3*2 = 11

// 실행 결과: 11