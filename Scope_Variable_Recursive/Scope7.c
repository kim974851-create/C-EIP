#include <stdio.h>
int g = 1;          // 전역변수 g = 1

void test(int g) {  // 매개변수 g (전역변수 g와 별개, 값만 복사받음)
    static int s = 2; // 정적 지역변수 s = 2 (최초 1회 초기화, 값 유지)
    int x = 3;        // 일반 지역변수 x = 1 (호출마다 초기화)
    g = g + 1;        // 매개변수 g만 변경 (전역변수 g에 영향 없음)
    s = s + 1;        // 정적변수 s 누적
    x = x + 1;        // 지역변수 x 변경
    printf("%d %d %d\n", g, s, x);
}

int main() {
    test(g); // [1] 전역 g=1 복사 → 매개변수 g=1+1=2, s=2+1=3, x=3+1=4 → 출력: 2 3 4
    test(g); // [2] 전역 g=1 복사 → 매개변수 g=1+1=2, s=3+1=4, x=3+1=4 → 출력: 2 4 4
    printf("%d\n", g); // [3] 전역변수 g 출력 → 1 (변화 없음)
    return 0;
}

// 실행 결과:
// 2 3 4
// 2 4 4
// 1