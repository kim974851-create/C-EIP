#include <stdio.h>
int main(void) {
    /*
     * 8진수: 앞에 0을 붙임
     *   010 = 1 × 8¹ + 0 × 8⁰
     *       = 8 + 0
     *       = 10진수 8
     */
    int a = 010;   // 8진수 010 = 10진수 8

    /*
     * 16진수: 앞에 0x를 붙임
     *   0x10 = 1 × 16¹ + 0 × 16⁰
     *        = 16 + 0
     *        = 10진수 16
     *
     * 16진수 자리값 참고:
     *   0~9 → 그대로
     *   A=10, B=11, C=12, D=13, E=14, F=15
     */
    int b = 0x10;  // 16진수 0x10 = 10진수 16

    printf("%d %d\n", a, b); // 8 16 출력
    return 0;
}