#include <stdio.h> // printf 함수를 사용하기 위한 표준 입출력 헤더 파일 포함

int main(void) { // 프로그램의 시작점, void는 매개변수 없음을 의미

    printf("%d %o %x\n", 10, 10, 10);
    // printf : 형식에 맞춰 출력하는 함수
    //
    // "%d %o %x\n" : 출력 형식 문자열
    //   %d  → 첫 번째 인자(10)를 10진수로 출력 → 10
    //   %o  → 두 번째 인자(10)를 8진수로 출력  → 12
    //            └ 변환 원리: 10 ÷ 8 = 1 나머지 2
    //                        나머지를 역순으로 읽으면 12
    //                        검산: 1×8 + 2 = 10 ✔
    //   %x  → 세 번째 인자(10)를 16진수로 출력 → a
    //            └ 변환 원리: 10 ÷ 16 = 0 나머지 10
    //                        16진수에서 10 = a
    //                        (10=a, 11=b, 12=c, 13=d, 14=e, 15=f)
    //   \n  → 줄바꿈
    //
    // 10, 10, 10 : 같은 숫자 10을 세 가지 진법으로 각각 출력

    // \n : 새 줄
    // \t : 탭만큼 띄우기
    printf("A\nB\tC");

    return 0; // main 함수 종료, 0을 반환하면 운영체제에 정상 종료를 알림
}