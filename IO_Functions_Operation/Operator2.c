#include <stdio.h>
int main(void) {
    int a = 10, b = 7; // a=10(이진수: 1010), b=7(이진수: 0111) 초기화

    /*
     * 비트 연산 과정:
     *   a = 1010
     *   b = 0111
     *   ─────────────────────────────────────
     *   & (AND) : 0010 = 2   → 둘 다 1인 자리만 1
     *   | (OR)  : 1111 = 15  → 하나라도 1이면 1
     *   ^ (XOR) : 1101 = 13  → 서로 다른 자리만 1
     */
    printf("%d %d %d\n", a & b, a | b, a ^ b); // 2 15 13 출력

    return 0;
}