#include<stdio.h>
int main(void) {
    char c = 'A'; // 'A'мқҳ ASCII мҪ”л“ң = 65

    /*
     * ASCII мҪ”л“ңн‘ң (A~F кө¬к°„)
     * в”Ңв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”җ
     * в”Ӯ A  в”Ӯ B  в”Ӯ C  в”Ӯ D  в”Ӯ E  в”Ӯ F  в”Ӯ
     * в”ңв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Ө
     * в”Ӯ 65 в”Ӯ 66 в”Ӯ 67 в”Ӯ 68 в”Ӯ 69 в”Ӯ 70 в”Ӯ
     * в””в”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”ҳ
     *   вҶ‘                       вҶ‘
     *   c='A'(65)        c+5='F'(70)
     *
     * c = c + 5 = 65 + 5 = 70 вҶ’ 'F'
     */
    c = c + 5;

    putchar(c);    // л¬ёмһҗлЎң м¶ңл Ҙ вҶ’ F
    putchar('\n'); // мӨ„л°”кҝҲ
    printf("%d\n", c); // мҲ«мһҗлЎң м¶ңл Ҙ вҶ’ 70

    return 0;
}