#include<stdio.h>
int main(void) {
    int x = 10; // x = 10 (이진수: 00001010)

    /*
     * << 1 (왼쪽 시프트 1칸)
     *   00001010  (10)
     * → 00010100  (20)  빈 자리는 0으로 채움
     *   2배 효과: 10 × 2¹ = 20
     *
     * >> 1 (오른쪽 시프트 1칸)
     *   00001010  (10)
     * → 00000101  (5)   밀려난 비트는 버려짐
     *   절반 효과: 10 ÷ 2¹ = 5
     */
    printf("%d %d\n", x << 1, x >> 1); // 20 5 출력

    return 0;
}