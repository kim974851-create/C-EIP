#include <stdio.h>
int main(void) {
    int a = 4, b = 7; // a=4(0100), b=7(0111)

    /*
     * a ^= b  →  a = a ^ b
     *   a = 0100  (4)
     *   b = 0111  (7)
     *   ─────────────
     *       0011  = 3  (서로 다른 비트만 1)
     *   결과: a = 3(0011), b = 7(0111)
     */
    a ^= b;

    /*
     * b &= a  →  b = b & a
     *   b = 0111  (7)
     *   a = 0011  (3)
     *   ─────────────
     *       0011  = 3  (둘 다 1인 비트만 1)
     *   결과: a = 3(0011), b = 3(0011)
     */
    b &= a;

    printf("%d %d\n", a, b); // 3 3 출력
    return 0;
}