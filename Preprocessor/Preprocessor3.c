#include <stdio.h>
#define STEP 3   // STEP → 3으로 바꿔치기
#define END  10  // END  → 10으로 바꿔치기

int main(void) {
    int x = 0;

    while (x < 10) {   // x가 10보다 작으면 계속 반복
        x += 3;        // x에 3을 더함
        if (x % 2 == 0)   // x를 2로 나눈 나머지가 0이면? (짝수면?)
            x++;           // x에 1을 더함
    }

    // ┌──────┬─────────┬──────────┬──────────────┬──────────────┬─────┐
    // │ 회차 │ x < 10? │  x += 3  │ x % 2 == 0?  │     x++      │  x  │
    // ├──────┼─────────┼──────────┼──────────────┼──────────────┼─────┤
    // │  1   │ 0 < 10✅│  0+3= 3  │ 3%2=1  → ❌  │  (생략)      │  3  │
    // │  2   │ 3 < 10✅│  3+3= 6  │ 6%2=0  → ✅  │  6+1= 7      │  7  │
    // │  3   │ 7 < 10✅│  7+3=10  │ 10%2=0 → ✅  │  10+1=11     │ 11  │
    // │  4   │11 < 10❌│  (탈출)  │              │              │ 11  │
    // └──────┴─────────┴──────────┴──────────────┴──────────────┴─────┘

    printf("%d\n", x);   // 출력: 11

    return 0;
}