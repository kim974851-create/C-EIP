#include <stdio.h>
#define LIMIT 5   // LIMIT를 숫자 5로 바꿔치기

int main(void) {
    int i, sum = 0;   // i = 반복용 숫자, sum = 합계 (초기값 0)

    for (i = 0; i < 5; i += 2) {
        //   ↑ 시작  ↑ 조건   ↑ 매 반복마다 i에 2씩 더함
        sum += i;   // sum = sum + i
    }

    // ┌──────┬─────┬──────────────┬─────┐
    // │ 회차 │  i  │   조건 i < 5 │ sum │
    // ├──────┼─────┼──────────────┼─────┤
    // │  1   │  0  │  0 < 5  ✅   │  0  │  sum = 0 + 0
    // │  2   │  2  │  2 < 5  ✅   │  2  │  sum = 0 + 2
    // │  3   │  4  │  4 < 5  ✅   │  6  │  sum = 2 + 4
    // │  4   │  6  │  6 < 5  ❌   │  6  │  조건 실패 → 탈출
    // └──────┴─────┴──────────────┴─────┘

    printf("%d\n", sum);   // 출력: 6

    return 0;
}