#include <stdio.h>

int main(void) {
    /*
     * do-while 은 "일단 한 번은 무조건 실행" !
     *
     *  x  | 출력      | x-- 후 | x>0 조건
     * ----+-----------+--------+----------
     *  0  | x = 0     |  x→-1  |  -1>0 ❌ 탈출
     * ----+-----------+--------+----------
     *  출력: x = 0
     *
     *  ⚠️ x가 처음부터 0이라 조건(x>0)은 거짓이지만
     *     do-while 이라 무조건 1번은 실행됨!
     */

    int x = 0;

    do {
        printf("x = %d\n", x);  // 무조건 1번 출력
        x--;                     // x를 1 깎음 → x = -1
    } while (x > 0);             // -1 > 0 ❌ → 바로 탈출

    return 0;
}