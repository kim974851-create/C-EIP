#include <stdio.h>

int main(void) {
    /*
     * --n 은 "먼저 깎고, 그 다음에 비교" !
     *
     *  들어올 때 n | --n 후 n | n>1 조건 | 출력
     * ------------+----------+----------+-----
     *      5      |    4     |  4>1 ✅  |  4
     *      4      |    3     |  3>1 ✅  |  3
     *      3      |    2     |  2>1 ✅  |  2
     *      2      |    1     |  1>1 ❌  |  탈출
     *
     *  출력: 4 3 2
     */

    int n = 5;

    while (--n > 1) {       // n을 먼저 1 깎고 → 1보다 크면 반복
        printf("%d ", n);   // 깎인 n을 출력
    }

    return 0;
}