#include <stdio.h>

int main(void) {
    /*
     * [전체 구조 - 1부터 5까지 합산]
     *
     *  i  | sum = sum + i
     * ----+---------------
     *  1  | 0  + 1  =  1
     *  2  | 1  + 2  =  3
     *  3  | 3  + 3  =  6
     *  4  | 6  + 4  = 10
     *  5  | 10 + 5  = 15
     * ----+---------------
     *             출력: 15
     */

    int i = 1;      // 반복 변수 초기화
    int sum = 0;    // 합계 변수 초기화

    while (i <= 5) {    // i가 5 이하인 동안 반복
        sum += i;       // sum에 i를 누적
        i++;            // i 1 증가 → 다음 수로 이동
    }

    printf("%d\n", sum);    // 결과 출력 → 15
    return 0;
}

