#include <stdio.h>

int main(void) {
    /*
     * [전체 구조 - 5부터 10까지의 합산]
     *
     *  i  | 조건(5<=i<=10) | sum = sum + i
     * ----+----------------+---------------
     *  1  |      skip      |  0
     *  2  |      skip      |  0
     *  3  |      skip      |  0
     *  4  |      skip      |  0
     *  5  |       OK       |  0  + 5  =  5
     *  6  |       OK       |  5  + 6  = 11
     *  7  |       OK       | 11  + 7  = 18
     *  8  |       OK       | 18  + 8  = 26
     *  9  |       OK       | 26  + 9  = 35
     * 10  |       OK       | 35  + 10 = 45
     * 11  |      skip      | 45
     * 12  |      skip      | 45
     * 13  |      skip      | 45
     * 14  |      skip      | 45
     * 15  |      skip      | 45
     * ----+----------------+---------------
     *                          출력: 45
     */

    int sum = 0;

    for (int i = 1; i <= 15; i++) {         // i = 1부터 15까지 반복
        if (i >= 5 && i <= 10) {            // 5 이상 10 이하일 때만
            sum += i;                        // sum에 i 누적
        }
    }

    printf("%d\n", sum);    // 결과 출력 → 45
    return 0;
}