#include <stdio.h>

int main(void) {
    /*
     * i > 4 가 되는 순간 break 로 즉시 탈출!
     *
     *  i  | i>4? | sum = sum + i
     * ----+------+--------------
     *  1  |  ❌  | 0  + 1 =  1
     *  2  |  ❌  | 1  + 2 =  3
     *  3  |  ❌  | 3  + 3 =  6
     *  4  |  ❌  | 6  + 4 = 10
     *  5  |  ✅  | break! → 즉시 탈출 (sum 그대로 10)
     * ----+------+--------------
     *  출력: 10
     */

    int sum = 0;

    for (int i = 1; i <= 10; i++) {  // i를 1부터 10까지 반복
        if (i > 4) {
            break;          // ⚠️ i가 5 되는 순간 즉시 탈출! sum 안 더함
        }
        sum += i;           // i가 4 이하일 때만 sum에 더함
    }

    printf("%d\n", sum);    // 결과 출력 → 10
    return 0;
}