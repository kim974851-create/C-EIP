#include <stdio.h>

int main(void) {
    /*
     * [전체 구조]
     *
     * i=1 : j = 4,3,2,1  ->  ****
     * i=2 : j = 4,3,2    ->  ***
     * i=3 : j = 4,3      ->  **
     * i=4 : j = 4        ->  *
     *
     * i가 커질수록 j의 시작점(4)은 고정,
     * 끝점(i)이 올라가므로 별이 1개씩 줄어듦
     */

    for (int i = 1; i <= 4; i++) {       // i = 행 번호 (1~4)
        for (int j = 4; j >= i; j--) {   // j = 4에서 i까지 감소
            printf("*");                  // 별 1개 출력
        }
        printf("\n");                     // 줄바꿈 → 다음 행으로
    }
    return 0;
}