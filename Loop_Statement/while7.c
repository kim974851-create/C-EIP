#include <stdio.h>

int main(void) {
    /*
     * continue 는 "이번 회차 나머지 건너뛰고 다시 위로!" !
     *
     *  i  | i%2==0?  | 동작
     * ----+----------+---------------------------
     *  1  |  1%2=1 ❌ | printf → 1 출력
     *  2  |  2%2=0 ✅ | continue → printf 건너뜀
     *  3  |  3%2=1 ❌ | printf → 3 출력
     *  4  |  4%2=0 ✅ | continue → printf 건너뜀
     *  5  |  5%2=1 ❌ | printf → 5 출력
     *  6  |  6%2=0 ✅ | continue → printf 건너뜀
     *  7  |  7%2=1 ❌ | printf → 7 출력
     *  8  |  i<7 ❌   | 탈출
     * ----+----------+---------------------------
     *  출력: 1 3 5 7
     */

    int i = 0;

    while (i < 7) {             // i가 7보다 작으면 계속 반복
        i++;                    // 먼저 i를 1 올림

        if (i % 2 == 0) {       // i를 2로 나눈 나머지가 0이면 → 짝수!
            continue;           // ⚠️ 짝수면 아래 printf 건너뛰고 다시 위로!
        }

        printf("%d ", i);       // 홀수만 여기까지 내려와서 출력됨
    }

    return 0;
}