#include <stdio.h>

int main(void) {
    /*
     * i++ 는 "출력 먼저, 증가 나중" !
     *
     *  i  | printf 출력 | 그 다음 i 값
     * ----+-------------+-------------
     *  0  |      0      |  i → 1
     *  1  |      1      |  i → 2
     *  2  |      2      |  i → 3
     * ----+-------------+-------------
     *  3  | (3<3 거짓)  |  while 탈출
     *
     *  출력: 0 1 2
     */

    int i = 0;

    while (i < 3) {             // i가 3보다 작으면 계속 반복
        printf("%d ", i++);     // i를 출력한 뒤에 i를 1 증가
    }

    return 0;
}