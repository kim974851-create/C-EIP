#include <stdio.h>

int main(void) {
    /*
     * break 는 "조건 만족하면 즉시 탈출" !
     *break 는 그 자리에서 즉시 탈출
     *continue 이번 회차만 건너뜀, 루프는 계속
     *  i  | i==4? | 출력 | i++
     * ----+-------+------+----
     *  1  |  ❌   |  1   | i→2
     *  2  |  ❌   |  2   | i→3
     *  3  |  ❌   |  3   | i→4
     *  4  |  ✅   | 탈출 | (i++도 출력도 없이 즉시 종료)
     * ----+-------+------+----
     *  출력:
     *  1
     *  2
     *  3
     */

    int i = 1;

    do {
        if (i == 4) {
            break;              // ⚠️ 즉시 탈출! 아래 printf, i++ 절대 실행 안 됨
        }
        printf("%d\n", i);      // break 안 걸리면 출력
        i++;                    // break 안 걸리면 증가
    } while (i <= 5);           // 5 이하면 반복

    return 0;
}