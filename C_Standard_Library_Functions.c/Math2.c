#include <stdio.h>
#include <math.h>

int main(void) {

    // ─────────────────────────────────────────
    // ceil(2.1) : 올림 (ceiling = 천장)
    //   2.1 → 천장 방향(위)으로 올림
    //   결과 = 3.0
    // ─────────────────────────────────────────
    double a = ceil(2.1);   // a = 3.0

    // ─────────────────────────────────────────
    // floor(3.8) : 내림 (floor = 바닥)
    //   3.8 → 바닥 방향(아래)으로 내림
    //   결과 = 3.0
    // ─────────────────────────────────────────
    double b = floor(3.8);  // b = 3.0

    // ─────────────────────────────────────────
    // round(2.5) : 반올림
    //   2.5 → 0.5 이상이므로 올림
    //   결과 = 3.0
    // ─────────────────────────────────────────
    double c = round(2.5);  // c = 3.0

    // ─────────────────────────────────────────
    // round(-2.5) : 음수 반올림
    //   -2.5 → 절댓값 기준 0.5 이상이므로 올림
    //        → 음수에서 올림 = 더 작은 수
    //   결과 = -3.0
    // ─────────────────────────────────────────
    double d = round(-2.5); // d = -3.0

    // %.0f : 소수점 0자리 출력 (정수처럼)
    printf("%.0f %.0f %.0f %.0f\n", a, b, c, d);
    // 출력 : 3 3 3 -3

    return 0;
}