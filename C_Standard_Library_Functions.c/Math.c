#include <stdio.h>
#include <math.h>

int main(void) {

    // ─────────────────────────────────────────
    // sqrt(16) : 16의 제곱근
    //   √16 = 4.0
    //
    // pow(2, 3) : 2의 3제곱
    //   2³ = 2 × 2 × 2 = 8.0
    //
    // x = 4.0 + 8.0 = 12.0
    // ─────────────────────────────────────────
    double x = sqrt(16) + pow(2, 3);

    // %.0f : 소수점 0자리까지 출력 (= 정수처럼 출력)
    // x = 12.0  →  "12" 출력
    printf("%.0f\n", x);  // 출력 : 12

    return 0;
}