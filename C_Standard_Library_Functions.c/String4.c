#include<stdio.h>
#include<string.h>

int main() {
    char a[] = "ABC";  // a = ['A','B','C','\0']
    char b[] = "ABD";  // b = ['A','B','D','\0']

    // ─────────────────────────────────────────
    // strcmp(a, b) : 문자열 전체를 앞에서부터 비교
    // [1] 'A'(65) vs 'A'(65) → 같음, 다음으로
    // [2] 'B'(66) vs 'B'(66) → 같음, 다음으로
    // [3] 'C'(67) vs 'D'(68) → 다름! 여기서 종료
    //      반환값 = 67 - 68 = -1 (음수 → a가 b보다 작다)
    // ─────────────────────────────────────────
    int r1 = strcmp(a, b);   // r1 = -1

    // ─────────────────────────────────────────
    // strncmp(a, b, 2) : 앞에서 2글자만 비교
    // [1] 'A'(65) vs 'A'(65) → 같음, 다음으로
    // [2] 'B'(66) vs 'B'(66) → 같음, n=2 도달 → 종료
    //      반환값 = 0 (두 문자열이 같다)
    // ─────────────────────────────────────────
    int r2 = strncmp(a, b, 2);  // r2 = 0

    // r1=-1, r2=0 출력
    printf("%d %d\n", r1, r2);  // 출력: -1 0

    return 0;
}