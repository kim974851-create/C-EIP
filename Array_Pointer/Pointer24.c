#include <stdio.h>

int main() {
    // [1] 문자열 포인터 배열 선언
    //     각 문자열은 읽기 전용 메모리에 저장됨
    //
    //     s[0] = 0xA00 → ['A','B','\0']
    //     s[1] = 0xB00 → ['C','D','\0']
    //     s[2] = 0xC00 → ['E','F','\0']
    //
    //     s:  [ 0xA00 | 0xB00 | 0xC00 ]
    //             ↓       ↓       ↓
    //            "AB"    "CD"    "EF"
    char *s[3] = {"AB", "CD", "EF"};

    // [2] pp = s
    //     s는 char** 로 decay → pp = &s[0]
    //
    //     pp
    //      ↓
    //     [ 0xA00 | 0xB00 | 0xC00 ]
    //        ↓        ↓       ↓
    //       "AB"     "CD"    "EF"
    char **pp = s;

    // [3] *pp[1]
    //     pp[1]  = *(pp+1) = s[1] = 0xB00  → char* ("CD" 시작주소)
    //     *pp[1] = *(0xB00) = 0번째 문자 = 'C'
    //
    //     연산자 우선순위: [] 가 * 보다 높음
    //     → *(pp[1]) 로 읽힘
    //
    //     pp+1 → s[1](0xB00) → [ >>C<< | D | \0 ]
    //                                ↑ *pp[1] = 'C'
    //
    //     %c 출력 → 'C'

    // [4] pp[2][1]
    //     pp[2]    = *(pp+2) = s[2] = 0xC00  → char* ("EF" 시작주소)
    //     pp[2][1] = *(0xC00+1) = 1번째 문자 = 'F'
    //
    //     pp+2 → s[2](0xC00) → [ E | >>F<< | \0 ]
    //                               [0]  [1]
    //                                     ↑ pp[2][1] = 'F'
    //
    //     %c 출력 → 'F'

    printf("%c %c\n", *pp[1], pp[2][1]);  // 출력: C F

    return 0;
}