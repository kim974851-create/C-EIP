#include <stdio.h>
int main(void) {
    int a = 10, b = 20;   // [1] a=10, b=20 선언
    int *p = &a;           // [2] p → a의 주소 (p는 a를 가리킴)
    int *q = &b;           // [3] q → b의 주소 (q는 b를 가리킴)

    *p = *p + 5;           // [4] *p = a = 10+5 = 15  (a가 15로 변경)
    //     현재: a=15, b=20

    q = p;                 // [5] q에 p의 주소를 복사
    //     → q도 이제 a를 가리킴 (b는 아무도 안 가리킴)
    //     현재: p→a, q→a

    *q = *q + 5;           // [6] *q = a = 15+5 = 20  (a가 20으로 변경)
    //     b는 건드리지 않음
    //     현재: a=20, b=20

    printf("%d, %d\n", a, b);  // [7] 출력: 20, 20

    return 0;
}