#include <stdio.h>

int main() {
    // [1] 변수 선언
    //     0x100: [ 1 ]  ← a
    //     0x200: [ 2 ]  ← b
    //     0x300: [ 3 ]  ← c
    int a = 1, b = 2, c = 3;

    // [2] 포인터 배열 선언
    //     arr은 int* 3개짜리 배열 (배열 원소가 포인터)
    //     arr[0] = &a = 0x100
    //     arr[1] = &b = 0x200
    //     arr[2] = &c = 0x300
    //
    //     메모리:
    //     arr[0]   arr[1]   arr[2]
    //     [0x100] [0x200] [0x300]
    //        ↓       ↓       ↓
    //       [1]     [2]     [3]
    //        a       b       c
    int *arr[3] = {&a, &b, &c};

    // [3] pp = arr
    //     arr은 int** 로 decay (배열의 첫 원소 주소)
    //     pp = &arr[0] → arr[0]이 가리키는 곳부터 읽기 시작
    //
    //     pp → arr[0](0x100) → a(1)
    //           arr[1](0x200) → b(2)
    //           arr[2](0x300) → c(3)
    int **pp = arr;

    // [4] pp[0][0]
    //     pp[0]    = *(pp+0) = arr[0] = 0x100  (int* 하나)
    //     pp[0][0] = *(arr[0]+0) = *0x100 = 1
    //
    //     역참조 경로:
    //     pp → arr[0](0x100) → a = 1
    //
    //     pp:  [ >>0x100<< | 0x200 | 0x300 ]
    //                ↓
    //              [ 1 ]  ← pp[0][0]

    // [5] pp[2][0]
    //     pp[2]    = *(pp+2) = arr[2] = 0x300  (int* 하나)
    //     pp[2][0] = *(arr[2]+0) = *0x300 = 3
    //
    //     역참조 경로:
    //     pp+2 → arr[2](0x300) → c = 3
    //
    //     pp:  [ 0x100 | 0x200 | >>0x300<< ]
    //                                ↓
    //                              [ 3 ]  ← pp[2][0]

    printf("%d %d\n", pp[0][0], pp[2][0]);  // 출력: 1 3

    return 0;
}