#include <stdio.h>

// change 함수: int x[3]은 배열의 "주소"를 받음
// → 그림에서 change()가 메모리(RAM)를 직접 가리키는 화살표!
void change(int x[3]) {
    // x[1]은 a[1]과 똑같은 메모리 칸 (주소 0x1004)
    // 그림의 노란 박스 "a[1]"을 직접 수정
    x[1] = x[1] + 10;  // 2 + 10 = 12 → 원본이 바뀜!
}

int main(void) {
    // 배열 a 생성: 메모리에 3칸 연속으로 잡힘
    // a = 배열의 시작 주소 (그림의 0x1000)
    int a[3] = {1, 2, 3};

    // change(a): 배열 이름 = 주소! 값 복사가 아닌 "주소"를 넘김
    // → 그림의 "change(a) 호출 - 주소를 넘김" 화살표
    change(a);

    // a[1]이 실제로 바뀌었음 → 출력: 1, 12, 3
    printf("%d, %d, %d\n", a[0], a[1], a[2]);

    return 0;
}