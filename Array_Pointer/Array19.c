#include <stdio.h>

int main() {
    // [1] 2행 3열 2차원 배열
    // a[0] → {10, 20, 30}
    // a[1] → {40, 50, 60}
    int a[2][3] = {
        {10, 20, 30},
        {40, 50, 60}
    };

    // [2] a[0][1]
    //     → 0행 1열
    //     → 20

    // [3] *(a[0] + 1)
    //     a[0]      → 0행 시작 주소 (10 위치)
    //     a[0] + 1  → 1칸 이동 (20 위치)
    //     *(..)     → 역참조 → 20
    //     → 20

    // [4] *(*(a + 1) + 0)
    //     a + 1     → 1행으로 이동
    //     *(a + 1)  → 1행 시작 주소 (40 위치)  =  a[1]
    //     + 0       → 그대로 (40 위치)
    //     *(..)     → 역참조 → 40
    //     → 40

    printf("%d %d %d\n", a[0][1], *(a[0] + 1), *(*(a + 1) + 0));

    return 0;
}