#include <stdio.h>

int main() {
    // [1] 2차원 배열 선언
    //     메모리 배치:
    //     주소:  +0   +4   +8   +12  +16  +20
    //     값:  [  1 |  2 |  3 |  4 |  5 |  6 ]
    //           ←── a[0]행 (12B) ──→←── a[1]행 (12B) ──→
    int a[2][3] = {{1, 2, 3}, {4, 5, 6}};

    // [2] p = a
    //     p → a[0] 행 시작주소 (int(*)[3] 타입)
    //     p+1 이동 시: 12바이트(int 3개) 점프
    //
    //     p → [  1 |  2 |  3 |  4 |  5 |  6 ]
    //          ↑ p (= p+0)         ↑ p+1
    int (*p)[3] = a;

    // [3] (*p)[2]
    //     *p      = *(p+0) = p[0] = a[0] 행 배열 자체
    //     (*p)[2] = a[0] 행의 2번째 열 → 3
    //
    //     역참조 순서: p → 행주소 → *p → 행배열 → [2] → 값
    //
    //     *p:  [ >>1<< |  2  | >>3<< ]
    //                          ↑ (*p)[2]
    printf("%d ", (*p)[2]);             // 출력: 3

    // [4] (*(p + 1))[0]
    //     p + 1     : 주소 +12 → a[1] 행 시작
    //     *(p + 1)  : a[1] 행 배열 자체 (= p[1])
    //     [0]       : a[1] 행의 0번째 열 → 4
    //
    //     역참조 순서: p+1 → 행주소 → *(p+1) → 행배열 → [0] → 값
    //
    //     *(p+1): [ >>4<< |  5  |  6  ]
    //                ↑ (*(p+1))[0]
    printf("%d ", (*(p + 1))[0]);       // 출력: 4

    // [5] *(*(p + 1) + 1)
    //     p + 1       : a[1] 행 시작주소
    //     *(p + 1)    : a[1] 행 배열 → int* 로 decay
    //     *(p + 1) + 1: a[1][0] 주소에서 +4바이트 → a[1][1] 주소
    //     *( ... )    : 역참조 → 5
    //
    //     역참조 순서: p+1 → *(p+1) → int* → +1 → *() → 값
    //
    //     *(p+1): [  4  | >>5<< |  6  ]
    //                      ↑ *(*(p+1)+1)
    printf("%d ", *(*(p + 1) + 1));     // 출력: 5

    return 0;
}