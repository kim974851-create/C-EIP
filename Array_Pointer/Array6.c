#include <stdio.h>

// x, y는 각각 a[1], a[2]의 "복사본" (Call by Value)
// → 주소가 아닌 값만 받으므로 원본을 건드릴 수 없음
void change(int x, int y) {
    int t;       // 교환용 임시 변수 (복사본들끼리만 사용)

    // 쉼표(,) = 문장 구분자 (세미콜론처럼 순서대로 실행)
    t = x,       // t = 2 (복사본)
    x = y,       // x = 3 (복사본)
    y = t;       // y = 2 (복사본)
    // 복사본끼리만 교환 → 함수 끝나면 전부 소멸
}

int main(void) {
    int a[3] = {1, 2, 3};

    // a[1]=2, a[2]=3의 "값"만 복사해서 전달 → 원본 불변
    change(a[1], a[2]);

    // a[1], a[2] 그대로 → 1, 2, 3
    printf("%d, %d, %d\n", a[0], a[1], a[2]);

    return 0;
}