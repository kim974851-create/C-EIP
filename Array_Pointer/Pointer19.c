#include <stdio.h>

int main() {
    // [1] 변수 선언
    //     0x100: [ 3 ]  ← x
    //     0x200: [ 7 ]  ← y
    int x = 3, y = 7;

    // [2] p = &x
    //     p는 x를 가리킴
    //     0x300: [ 0x100 ]  ← p
    //
    //     p → x
    //     [0x100] → [ 3 ]
    int *p = &x;

    // [3] pp = &p
    //     pp는 p를 가리킴
    //     0x400: [ 0x300 ]  ← pp
    //
    //     pp → p → x
    //     [0x300] → [0x100] → [ 3 ]
    int **pp = &p;

    // [4] *pp = &y   ← 핵심!
    //     *pp = p이므로, p에 &y를 대입
    //     p가 x에서 y로 방향을 바꿈
    //
    //     변경 전:  pp → p → x
    //               [0x300] → [0x100] → [ 3 ]
    //
    //     변경 후:  pp → p → y
    //               [0x300] → [0x200] → [ 7 ]
    //
    //     p의 값이 0x100(x주소) → 0x200(y주소)으로 교체됨
    //     x는 건드리지 않음, p가 가리키는 대상만 바뀜
    *pp = &y;

    // [5] *p
    //     p가 이제 y를 가리킴 → *p = y의 값 = 7
    //
    // [6] **pp
    //     *pp  = p        (pp → p)
    //     **pp = *p = 7   (p → y → 7)
    //
    //     둘 다 같은 곳(y)을 가리키므로 결과 동일
    printf("%d %d\n", *p, **pp);   // 출력: 7 7

    return 0;
}