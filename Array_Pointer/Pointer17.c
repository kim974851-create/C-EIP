#include <stdio.h>

int main() {
    // [1] 1차원 배열 선언
    //     주소:  +0   +4   +8   +12
    //     값:  [  5 | 10 | 15 | 20 ]
    //           [0]  [1]  [2]  [3]
    int arr[4] = {5, 10, 15, 20};

    // [2] 강제 형변환으로 2차원처럼 재해석
    //     (int (*)[2]) : 2칸씩 끊어서 행으로 읽겠다는 선언
    //     메모리는 그대로, stride만 8바이트(int 2개)로 변경
    //
    //     p가 보는 구조:
    //     p[0] → [  5 | 10 ]   ← p+0 (주소 +0)
    //     p[1] → [ 15 | 20 ]   ← p+1 (주소 +8)
    int (*p)[2] = (int (*)[2])arr;

    // [3] 첫 번째 printf: p[0][0] 과 (*p)[0]
    //
    //     p[0][0]
    //     └─ p+0 → 0행 시작(주소 +0), [0] → +0B → 값 5
    //
    //     (*p)[0]
    //     └─ *p = *(p+0) = p[0] = 0행 배열
    //        [0] → 0행의 0번째 → 값 5
    //
    //     ※ p[0][0] == (*p)[0]  : 완전히 같은 주소
    //
    //     p[0]: [ >>5<< | 10 ]
    //              ↑ 둘 다 여기
    printf("%d %d\n", p[0][0], (*p)[0]);   // 출력: 5 5

    // [4] 두 번째 printf: p[1][0] 과 (*p)[2]
    //
    //     p[1][0]
    //     └─ p+1 → 1행 시작(주소 +8), [0] → +0B → 값 15
    //
    //     (*p)[2]
    //     └─ *p = p[0] = 0행 배열 → int* 로 decay
    //        [2] → 0행 시작에서 +8B(int 2개) → 주소 +8 → 값 15
    //
    //     ※ p[1][0] == (*p)[2]  : 다른 표현, 같은 주소
    //        p[1][0] → 행 단위로 +1 이동 후 0열
    //        (*p)[2] → 열 단위로 +2 이동 (행 경계 무시)
    //
    //     메모리: [  5 | 10 | >>15<< | 20 ]
    //                          ↑ 둘 다 여기
    printf("%d %d\n", p[1][0], (*p)[2]);   // 출력: 15 15

    return 0;
}

