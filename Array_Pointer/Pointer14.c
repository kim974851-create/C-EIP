#include <stdio.h>

int main() {
    // [1] 3행 2열 배열 선언
    //     메모리 배치 (연속):
    //     주소:  +0   +4   +8   +12  +16  +20
    //     값:  [ 10 | 20 | 30 | 40 | 50 | 60 ]
    //           a[0]행(8B)  a[1]행(8B)  a[2]행(8B)
    int a[3][2] = {{10, 20}, {30, 40}, {50, 60}};

    // [2] p = a + 1  ← 핵심!
    //     a        = &a[0] = 주소 +0
    //     a + 1    = &a[1] = 주소 +8  (int[2] = 8바이트 점프)
    //     p는 a[0]이 아닌 a[1] 행의 시작을 가리킴
    //
    //     a → [ 10 | 20 | 30 | 40 | 50 | 60 ]
    //                      ↑
    //               p ────┘   (p[0] = a[1] 행)
    int (*p)[2] = a + 1;

    // [3] 첫 번째 printf
    //     a[0][0] : a 기준 0행 0열 → 10
    //     p[0][0] : p 기준 0행 0열
    //               p+0 → a[1] 행, +0열 → 30
    //
    //     a 기준:  [ >>10<< | 20 | 30  | 40 | 50 | 60 ]
    //                 a[0][0]↑
    //     p 기준:  [  10  | 20 | >>30<< | 40 | 50 | 60 ]
    //                              p[0][0]↑
    printf("%d %d\n", a[0][0], p[0][0]);    // 출력: 10 30

    // [4] 두 번째 printf
    //     a[1][1] : a 기준 1행 1열 → 40
    //     p[1][1] : p 기준 1행 1열
    //               p+1 → a[2] 행(+8바이트), +1열 → 60
    //
    //     a 기준:  [ 10 | 20 | 30 | >>40<< | 50 | 60 ]
    //                              a[1][1]↑
    //     p 기준:  [ 10 | 20 | 30 |  40  | 50 | >>60<< ]
    //                                            p[1][1]↑
    printf("%d, %d\n", a[1][1], p[1][1]);   // 출력: 40, 60

    return 0;
}