#include <stdio.h>

int main() {
    // [1] 2행 3열 2차원 배열 선언
    // a[0] → {1, 2, 3}
    // a[1] → {4, 5, 6}
    int a[2][3] = {
        {1, 2, 3},
        {4, 5, 6}
    };

    // [2] 바깥 루프: 행(i) 0 → 1 반복
    for (int i = 0; i < 2; i++) {

        // [3] 안쪽 루프: 열(j) 0 → 2 반복
        for (int j = 0; j < 3; j++) {

            // [4] a[i][j] 출력
            // i=0, j=0 → a[0][0] = 1
            // i=0, j=1 → a[0][1] = 2
            // i=0, j=2 → a[0][2] = 3
            // i=1, j=0 → a[1][0] = 4
            // i=1, j=1 → a[1][1] = 5
            // i=1, j=2 → a[1][2] = 6
            printf("%d ", a[i][j]);
        }

        // [5] 한 행 끝나면 줄바꿈
        printf("\n");
    }

    return 0;
}