#include <stdio.h>

// change 함수: int x는 "복사본"을 받는 공간
// → 그림에서 오른쪽 change() 박스 안의 x
void change(int x) {        // x = a[1]의 복사본 (값만 복사됨)
    x = x + 10;             // 복사본 x: 2 → 12 (그림의 빨간 박스)
    // 원본 a[1]은 전혀 건드리지 않음!
}                           // 함수 끝 → x 소멸 (휴지통행)

int main(void) {
    // 메모리에 배열 3칸 생성 (그림의 파란 박스)
    // a[0]=1, a[1]=2(노란 박스), a[2]=3
    int a[3] = { 1, 2, 3 };

    // a[1]의 값(2)만 복사해서 change()에 전달 (그림의 주황 화살표)
    // → "Call by Value": 주소가 아닌 값만 넘김
    change(a[1]);

    // a[1]은 여전히 2 → 출력: 1, 2, 3
    printf("%d, %d, %d\n", a[0], a[1], a[2]);

    return 0;
}