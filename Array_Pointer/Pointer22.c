#include <stdio.h>

int main() {
    // [1] 배열 선언
    //     주소:  +0   +4   +8   +12
    //     값:  [  1 |  2 |  3 |  4 ]
    //           [0]  [1]  [2]  [3]
    int arr[4] = {1, 2, 3, 4};

    // [2] p = arr
    //     p는 arr[0]의 주소를 담음 (int* 타입)
    //     p = 0x100 (예시)
    //
    //     p → [ 1 | 2 | 3 | 4 ]
    //           ↑ p (주소 +0)
    int *p = arr;

    // [3] pp = &p
    //     pp는 p의 주소를 담음 (int** 타입)
    //     pp = 0x200 (예시)
    //
    //     pp → p(0x100) → arr[0](1)
    //     [0x100] → [  1 |  2 |  3 |  4 ]
    int **pp = &p;

    // [4] **pp
    //     1단계: *pp  = p        = 0x100  (arr 시작주소)
    //     2단계: **pp = *p       = arr[0] = 1
    //
    //     pp → p(0x100) → [ >>1<< | 2 | 3 | 4 ]
    //                         ↑ **pp = 1

    // [5] *(*pp + 3)
    //     1단계: *pp      = p        = 0x100  (arr 시작주소)
    //     2단계: *pp + 3  = 0x100+12 = 0x10C  (arr[3] 주소)
    //     3단계: *(*pp+3) = arr[3]   = 4
    //
    //     *pp:  [ 1 | 2 | 3 | >>4<< ]
    //            +0  +4  +8   +12
    //                           ↑ *(*pp+3) = 4

    printf("%d %d\n", **pp, *(*pp + 3));  // 출력: 1 4

    return 0;
}