#include <stdio.h>

int main() {
    // [1] 변수 선언
    //     0x100: [ 3 ]  ← a
    //     0x200: [ 6 ]  ← b
    //     0x300: [ 9 ]  ← c
    int a = 3, b = 6, c = 9;

    // [2] 포인터 배열 선언
    //     pa[0] = &a = 0x100
    //     pa[1] = &b = 0x200
    //     pa[2] = &c = 0x300
    //
    //     pa:  [ 0x100 | 0x200 | 0x300 ]
    //              ↓       ↓       ↓
    //             [3]     [6]     [9]
    //              a       b       c
    int *pa[3] = {&a, &b, &c};

    // [3] pp = pa
    //     pa는 int** 로 decay → pp = &pa[0]
    //     pp → pa[0](0x100) → a(3)
    //
    //     pp
    //      ↓
    //     [ 0x100 | 0x200 | 0x300 ]
    //        ↓        ↓       ↓
    //       [3]      [6]     [9]
    int **pp = pa;

    // [4] pp++  ← 핵심!
    //     pp가 pa[0]에서 pa[1]으로 한 칸 이동
    //     int* 한 칸 = 포인터 크기(8바이트) 이동
    //
    //     pp++ 전:           pp++ 후:
    //      ↓                      ↓
    //     [ 0x100 | 0x200 | 0x300 ]
    //        ↓        ↓       ↓
    //       [3]      [6]     [9]
    //
    //     pp는 이제 pa[1]을 가리킴 → *pp = 0x200 → b = 6
    pp++;

    // [5] **pp
    //     1단계: *pp  = pa[1] = 0x200  (b의 주소)
    //     2단계: **pp = *0x200 = b = 6
    //
    //     pp → pa[1](0x200) → [ >>6<< ]
    //                               ↑ **pp = 6

    // [6] *(*(pp) + 0)
    //     *(pp)      = *pp = pa[1] = 0x200  (b의 주소)
    //     *(pp) + 0  = 0x200 + 0 = 0x200    (이동 없음)
    //     *(0x200)   = b = 6
    //
    //     +0은 아무 이동 없음 → **pp와 완전히 동일
    //
    //     pp → pa[1](0x200) → [ >>6<< ]
    //                               ↑ *(*(pp)+0) = 6

    printf("%d %d\n", **pp, *(*(pp) + 0));  // 출력: 6 6

    return 0;
}