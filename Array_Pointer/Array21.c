#include <stdio.h>

int main() {
    // [1] 2행 3열 2차원 배열
    // a[0] → {1, 2, 3}
    // a[1] → {4, 5, 6}
    int a[2][3] = {
        {1, 2, 3},
        {4, 5, 6}
    };

    // [2] p = a[1]
    //     p → 1행 시작 주소 (4 위치)
    int *p = a[1];

    // [3] *(p - 1)
    //     p     → 4 위치
    //     p - 1 → 한 칸 뒤로 → 3 위치
    //     *(..) → 역참조 → 3
    //
    // [4] *p
    //     p     → 4 위치
    //     *(..) → 역참조 → 4
    //
    // [5] *(p + 2)
    //     p     → 4 위치
    //     p + 2 → 두 칸 앞으로 → 6 위치
    //     *(..) → 역참조 → 6

    printf("%d %d %d\n", *(p - 1), *p, *(p + 2));

    return 0;
}