#include <stdio.h>

int main() {
    // [1] 2행 3열 2차원 배열 선언
    int a[2][3] = {
        {1, 2, 3},
        {4, 5, 6}
    };

    // [2] a[1] == *(a + 1) 비교
    //
    // a      → 배열 전체의 시작 주소 (0행을 가리킴)
    // a + 1  → 1행으로 이동한 주소
    // *(a+1) → 1행을 역참조 → 1행의 시작 주소
    // a[1]   → 1행의 시작 주소
    //
    // 둘 다 같은 주소 → 1 (참)
    printf("%d\n", a[1] == *(a + 1));

    // [3] a[1] == a[0] 비교
    //
    // a[0] → 0행의 시작 주소
    // a[1] → 1행의 시작 주소
    //
    // 서로 다른 주소 → 0 (거짓)
    printf("%d\n", a[1] == a[0]);

    return 0;
}