#include <stdio.h>

int main() {
    // [1] 일반 변수 선언
    //     메모리 어딘가에 int 4바이트 공간 확보
    //     주소: 예) 0x100
    //     값:  [ 10 ]
    int a = 10;

    // [2] 포인터 선언
    //     p는 int* 타입 → a의 주소를 저장
    //     주소: 예) 0x200
    //     값:  [ 0x100 ]  ← a의 주소
    //
    //     p → a
    //     0x200    0x100
    //     [0x100] → [ 10 ]
    int *p = &a;

    // [3] 이중 포인터 선언
    //     pp는 int** 타입 → p의 주소를 저장
    //     주소: 예) 0x300
    //     값:  [ 0x200 ]  ← p의 주소
    //
    //     pp → p → a
    //     0x300    0x200    0x100
    //     [0x200] → [0x100] → [ 10 ]
    int **pp = &p;

    // [4] **pp 역참조 2단계
    //
    //     1단계: *pp
    //            pp가 가리키는 곳(0x200) → p의 값 → 0x100
    //
    //     2단계: **pp = *(*pp) = *(0x100)
    //            p가 가리키는 곳(0x100) → a의 값 → 10
    //
    //     역참조 경로:
    //     pp(0x300) → *pp(0x200) → **pp(0x100) → 값 10
    //      ↓               ↓               ↓
    printf("%d\n", **pp);
    return 0;
}