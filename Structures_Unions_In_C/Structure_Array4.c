#include <stdio.h>

struct item {
    int a;  // 4바이트
    int b;  // 4바이트
};

int main(void) {
    // [1] 구조체 배열 초기화
    //     {1, 2} → arr[0].a=1, arr[0].b=2
    //     {3}    → arr[1].a=3, arr[1].b=0  ← b 생략 시 자동으로 0
    //     {0, 0} → arr[2].a=0, arr[2].b=0
    //
    //     ┌───────────┬───────────┬───────────┐
    //     │ a=1  b=2  │ a=3  b=0  │ a=0  b=0  │
    //     └───────────┴───────────┴───────────┘
    //         arr[0]      arr[1]      arr[2]
    struct item arr[3] = {
        {1, 2}, {3}, {0, 0}
    };

    // [2] arr[2].a = arr[0].b + arr[1].b
    //     arr[0].b = 2
    //     arr[1].b = 0
    //     2 + 0 = 2  → arr[2].a에 대입
    //
    //     ┌───────────┬───────────┬───────────┐
    //     │ a=1  b=2  │ a=3  b=0  │ a=2  b=0  │  ← arr[2].a만 변경
    //     └───────────┴───────────┴───────────┘
    arr[2].a = arr[0].b + arr[1].b;

    // [3] printf
    //     arr[0].a = 1
    //     arr[1].b = 0
    //     arr[2].a = 2
    printf("%d %d %d\n", arr[0].a, arr[1].b, arr[2].a);  // 출력: 1 0 2

    return 0;
}