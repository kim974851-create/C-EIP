#include <stdio.h>

struct info {
    char  c;   // 1바이트
    int   x;   // 4바이트
    short s;   // 2바이트
};
// 단순 합계 = 1 + 4 + 2 = 7바이트
// BUT 패딩 때문에 실제는 12바이트

int main(void) {
    // sizeof(struct info) = 12
    printf("%zu\n", sizeof(struct info));  // 출력: 12
    return 0;
}
// 총 8바이트  ← 12에서 4바이트 절약!

// 큰 것부터 작은 순서로 선언하면 패딩 최소화
// int(4) → short(2) → char(1) 순서가 가장 효율적
// 규칙 1. 각 멤버는 자신의 크기 배수 주소에 위치해야 함
//
//   char  c  → 1바이트 정렬 → +0 배치 OK
//   int   x  → 4바이트 정렬 → +1은 4의 배수 아님 → +4로 밀림
//                              +1~+3 패딩 3바이트 삽입
//   short s  → 2바이트 정렬 → +8 배치 OK (8은 2의 배수)
//
// 규칙 2. 구조체 전체 크기는 가장 큰 멤버(int=4B)의 배수여야 함
//
//   현재 끝: +8(s 시작) + 2(s 크기) = +10
//   4의 배수로 맞추려면 → 12 (패딩 2바이트 추가)
//
// 규칙 3. 최종 크기
//
//   c(1) + 패딩(3) + x(4) + s(2) + 패딩(2) = 12바이트