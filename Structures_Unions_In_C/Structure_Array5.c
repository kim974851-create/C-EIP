#include <stdio.h>

struct Num { int v; };

int main(void) {
    // [1] 초기화
    //     a[0].v = 1
    //     a[1].v = 2
    //     a[2].v = 3
    //     i = 0
    struct Num a[3] = {{1}, {2}, {3}};
    int i = 0;

    // [2] a[i].v = a[i].v + a[i+1].v
    //     i = 0 이므로:
    //     a[0].v = a[0].v + a[1].v
    //            = 1 + 2 = 3
    //
    //     a[0].v: 1 → 3
    //     a[1].v: 2 (그대로)
    //     a[2].v: 3 (그대로)
    a[i].v = a[i].v + a[i + 1].v;

    // [3] i = i + 1
    //     i: 0 → 1
    i = i + 1;

    // [4] a[i+1].v = a[i].v * 2
    //     i = 1 이므로:
    //     a[1+1].v = a[1].v * 2
    //     a[2].v   = 2 * 2 = 4
    //
    //     ※ a[i].v = a[1].v = 2  (그대로, [2]에서 안 건드렸음)
    //
    //     a[0].v: 3 (그대로)
    //     a[1].v: 2 (그대로)
    //     a[2].v: 3 → 4
    a[i + 1].v = a[i].v * 2;

    // [5] 최종
    //     a[0].v = 3
    //     a[1].v = 2
    //     a[2].v = 4
    printf("%d %d %d\n", a[0].v, a[1].v, a[2].v);  // 출력: 3 2 4

    return 0;
}